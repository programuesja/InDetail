package ArithmeticOperators;

public class DivisionOpTest {
    public static void main(String[] args) {
        int a = 10;
        int b = 7;
        double c = 10.3;
        double d = 3.5;

        System.out.println("a / b = " + (a / b));
        System.out.println("c / a = " + (c / a));
        System.out.println("a / d = " + (a / d));
        System.out.println("c / d = " + (c / d));
    }
}
